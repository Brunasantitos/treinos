var x = 6;
var y = 3;

// Operador de soma:
console.log(x += y);

// Operador de subtração:

console.log(x -= 2);

// Operador de multiplicação:

console.log(x *= y);

// Operador de divisão:

console.log(x /= y);

// utilizados em loops:
x++;
x--;

/* OBS: o valor da variável muda ao decorrer que o código carrega, ou seja o valor da variável que está acima muda
com a soma, ou seja, a lógica debaixo que vai receber a variável do valor acima, recebe o valor final da variável de cima */